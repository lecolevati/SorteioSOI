package pojo;

public class Sorteio {

	public String nomeLider;
	public String nomeTema;
	public int codLider;
	public int codTema;
	public String turno;
	public String Data;
	
	/**
	 * @return the data
	 */
	public String getData() {
		return Data;
	}
	/**
	 * @param data the data to set
	 */
	public void setData(String data) {
		Data = data;
	}
	/**
	 * @return the nomeLider
	 */
	public String getNomeLider() {
		return nomeLider;
	}
	/**
	 * @param nomeLider the nomeLider to set
	 */
	public void setNomeLider(String nomeLider) {
		this.nomeLider = nomeLider;
	}
	/**
	 * @return the nomeTema
	 */
	public String getNomeTema() {
		return nomeTema;
	}
	/**
	 * @param nomeTema the nomeTema to set
	 */
	public void setNomeTema(String nomeTema) {
		this.nomeTema = nomeTema;
	}
	/**
	 * @return the codLider
	 */
	public int getCodLider() {
		return codLider;
	}
	/**
	 * @param codLider the codLider to set
	 */
	public void setCodLider(int codLider) {
		this.codLider = codLider;
	}
	/**
	 * @return the codTema
	 */
	public int getCodTema() {
		return codTema;
	}
	/**
	 * @param codTema the codTema to set
	 */
	public void setCodTema(int codTema) {
		this.codTema = codTema;
	}
	/**
	 * @return the turno
	 */
	public String getTurno() {
		return turno;
	}
	/**
	 * @param turno the turno to set
	 */
	public void setTurno(String turno) {
		this.turno = turno;
	}
	
}
